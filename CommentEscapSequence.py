'''
This is multi line comment
Since Python will ignore string literals that are not assigned to a variable,
you can add a multiline string (triple quotes) in your code,
and place your comment inside it:
'''

# How to print two sentence in single line

print("This is a single line comment, ", end=" ")  # if you use end=" " the print function will not print the next line
print("this is multi line comment")

